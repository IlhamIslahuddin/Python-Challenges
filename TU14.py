mystring = "abcdefghijklmnopqrstuvwxyz"
print (mystring)
print (mystring[0:3]) #prints the first 3 letters
print (mystring[3::]) # prints the whole string starting from the 4rd character
print (mystring[0:6:2]) #the third number is the step between the characters
print(mystring[-1:-7:-1]) #prints backwards and stops before last character
print(mystring[0:-2])  #print until two from the last character
